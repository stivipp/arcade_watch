    {
        // Validation of Forms.
        request()->validate([
            'text' => 'required',
            'post_id' => 'required|integer|exists:posts,id',
        ]);


        // Creating Comments by user on specific Post.    
        auth()->user()->comments()->create(
            $request->all()
        );

   
        // Redirection to main page.
        return redirect()->back()
            ->with('flash', 'you totally Commented!');

    }

